function text({ font, size, x, y, value, color, kerning = 0 }) {
  return [
    "-font",
    font,
    "-pointsize",
    String(size),
    "-kerning",
    String(kerning),
    "-fill",
    color,
    "-stroke",
    "none",
    "-annotate",
    `+${x}+${y}`,
    value,
  ];
}

function line({ x1, y1, x2, y2, color, width = 7 }) {
  return [
    "-stroke",
    color,
    "-strokewidth",
    String(width),
    "-fill",
    "none",
    "-draw",
    `line ${x1},${y1} ${x2},${y2}`,
  ];
}

